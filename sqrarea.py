"""
sqrarea
-------------
Compute the area of a square/rectangle.
"""

def sqrarea(length, width):
    """
    Compute the area of a square/rectangle.

    Parameters
    ----------
    length: `float`
        Length of square/rectangle in centimeters.

    width: `float`
        Width of square/rectangle in centimeters.

    Returns
    -------
    area: `float`
        Area of square/rectangle in centimeters.
    """
    area = length*width
    return area
